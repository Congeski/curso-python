"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Vinicius Congeski')
print(1, 'Vinicius "Congeski"')

# Aspas duplas
print(2, "Vinicius 'Congeski'")

# Escape
print("Vinicius \"Congeski\"")

# r
print(r"Vinicius \"Congeski\"")